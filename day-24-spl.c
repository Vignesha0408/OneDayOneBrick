#include <stdio.h>

int main() {

    int n=25;

    for(int i=0;i<n/2+1;i++)
    {

    for(int j=0;j<=n;j++)
        if(i>=j ||i+j>=n)
            printf(" * ");
        else
            printf("   ");
    printf("\n");


    }


    return 0;
}


